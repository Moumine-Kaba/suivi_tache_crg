/**
 * Ajoute une introduction professionnelle à la section
 */
function addProfessionalIntroduction(sectionKey, text) {
  const introductions = {
    objectifs: 'Durant cette semaine, les objectifs que je m\'étais fixés étaient les suivants :',
    realisations: 'Durant cette semaine, j\'ai pu accomplir les réalisations suivantes :',
    difficultes: 'Au cours de cette semaine, j\'ai rencontré les difficultés suivantes :',
    solutions: 'Pour faire face à ces difficultés, j\'ai mis en place les solutions suivantes :',
    besoins: 'Afin d\'améliorer mon efficacité, j\'ai identifié les besoins suivants :',
    perspectives: 'Pour la semaine prochaine, mes perspectives sont les suivantes :',
  }
  
  const intro = introductions[sectionKey]
  if (intro && !text.toLowerCase().includes(intro.toLowerCase().substring(0, 20))) {
    return `${intro}\n\n${text}`
  }
  
  return text
}

/**
 * Envoie automatiquement le rapport au directeur concerné (celui de la direction de l'expéditeur)
 */
export async function sendReportToDirectrice(reportId) {
  try {
    const { directionsService } = await import('./api.js')

    // Récupérer le rapport
    const { data: report } = await supabase
      .from('reports')
      .select('*')
      .eq('id', reportId)
      .single()

    if (!report) {
      throw new Error('Rapport non trouvé')
    }

    // Trouver le directeur de la direction de l'expéditeur (report.direction = nom du service)
    let directorId = await directionsService.getDirectorIdForService(report.direction)
    if (!directorId) {
      directorId = await directionsService.getDirectorIdForUserId(report.user_id)
    }
    if (!directorId) {
      // Fallback : premier directeur trouvé (rétrocompatibilité)
      const { data: fallback } = await supabase.from('users').select('id').eq('role', 'directrice').limit(1).maybeSingle()
      directorId = fallback?.id
    }
    if (!directorId) {
      throw new Error('Directeur/directrice non trouvé(e) pour cette direction')
    }

    // Créer une notification pour le directeur concerné
    const { data: notification, error } = await supabase
      .from('notifications')
      .insert({
        user_id: directorId,
        title: 'Nouveau rapport hebdomadaire',
        message: `${report.user_name} a envoyé son rapport hebdomadaire pour la semaine du ${new Date(report.week).toLocaleDateString('fr-FR')}`,
        type: 'info',
        read: false,
        metadata: JSON.stringify({
          report_id: reportId,
          user_id: report.user_id,
          week: report.week,
        }),
      })
      .select()
      .single()

    if (error) {
      console.error('❌ Erreur lors de la création de la notification:', error)
      throw error
    }

    console.log('✅ Notification envoyée au directeur concerné pour le rapport:', reportId)
    return notification
  } catch (error) {
    console.error('❌ Erreur lors de l\'envoi du rapport au directeur:', error)
    throw error
  }
}

